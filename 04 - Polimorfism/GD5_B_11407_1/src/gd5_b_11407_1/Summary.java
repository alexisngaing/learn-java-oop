public class Summary {
    public Summary() {
        
    }
    
    public double count(int sks, double hargaSKS) {
        return sks * hargaSKS;
    }
    
    public int count(int sks1, int sks2, int sks3) {
        return sks1 + sks2 + sks3;
    }
}
