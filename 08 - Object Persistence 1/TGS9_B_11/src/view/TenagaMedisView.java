    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (getAnswer == JOptionPane.YES_OPTION) {
            try {
                tmControl.deleteDataTenagaMedis(idInput.getText());
                clearText();
                showTenagaMedis();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus...");
            } catch (Exception e) {
                System.out.println("Error : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak jadi dihapus!");
        }
        
        editBtn.setEnabled(false);
        deleteBtn.setEnabled(false);
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(true);
        setComponent(false);

        try {
            TenagaMedis tenagamedis = tmControl.searchTenagaMedis(searchInput.getText());
            if (tenagamedis == null) {
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan [!]", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            } else {
                idInput.setText(tenagamedis.getIdTenagaMedis());
                namaInput.setText(tenagamedis.getNama());
                tahunInput.setText("" + tenagamedis.getTahunMasuk());
                noTeleponInput.setText(tenagamedis.getNoTelepon());
                npaidiInput.setText(tenagamedis.getNpaidi());
                nirappniInput.setText("" + tenagamedis.getPpni());
                if (npaidiInput.getText().isEmpty()) {
                    perawatRadioBtn.setSelected(true);
//                    dokterRadioBtn.setSelected(false);
                    npaidiInput.setEnabled(false);
                } else {
                    dokterRadioBtn.setSelected(true);
//                    perawatRadioBtn.setSelected(false);
                    nirappniInput.setEnabled(false);
                }
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

